import React from "react";
import { Button, Text, View } from "react-native";
import { useNavigation } from "@react-navigation/native";
import { createNativeStackNavigator } from "@react-navigation/native-stack";
import SendEgg from "../Send_Egg/SendEgg";

const Stack = createNativeStackNavigator();

export default function EggNavigator() {  //check here
    <Stack.Navigator>
        <Stack.Screen name="Send Egg" component={SendEgg} />
    </Stack.Navigator>
}

export default function SendEggIcon() {
    const nav = useNavigation();

    return (
    <>
      <View>
          <Text>SendEgg Icon here</Text>
          <Button title="Send Egg Icon" onPress={() => nav.navigate('SendEgg')}></Button>  
      </View>
    </>
    )
}